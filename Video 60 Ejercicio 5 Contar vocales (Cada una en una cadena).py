"""
Ejercicio 5

Hacer un programa donde se encuentre cada una de las vocales en una cadena, mostrar
conteo de las apariciones de cada vocal.
"""

Cadena = input("Ingrese una frase o palabra a analizar: ")

print(f"\nVocal a {Cadena.count('a')}")
print(f"Vocal e {Cadena.count('e')}")
print(f"Vocal i {Cadena.count('i')}")
print(f"Vocal o {Cadena.count('o')}")
print(f"Vocal u {Cadena.count('u')}")


